export type PreferenceContextValue = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  darkMode: boolean;
  setDarkMode: (enabled: boolean) => void;
  t: (key: keyof (typeof translations)['English']) => string;
  tx: (text: string) => string;
  theme: ThemePalette;
};

export const PreferenceContext = createContext<PreferenceContextValue | null>(null);
export function usePreferenceContext() {
  const value = useContext(PreferenceContext);
  if (!value) throw new Error('PreferenceContext missing');
  return value;
}

type PreferenceValueParams = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  darkMode: boolean;
  setDarkMode: (enabled: boolean) => void;
  currentRole?: UserRole;
};

export function usePreferenceValue({
  language,
  setLanguage,
  darkMode,
  setDarkMode,
  currentRole = 'electrician',
}: PreferenceValueParams): PreferenceContextValue {
  const theme = useMemo(() => getThemePalette(darkMode, currentRole), [currentRole, darkMode]);

  return useMemo(
    () => ({
      language,
      setLanguage,
      darkMode,
      setDarkMode,
      t: (key: keyof (typeof translations)['English']) => getSafeTranslation(language, key),
      tx: (text: string) => translateUiText(language, text),
      theme,
    }),
    [currentRole, darkMode, language, setDarkMode, setLanguage, theme]
  );
}






